# A short program solving the Pythagorean theorem mathematical problem.
# Pythagorean theorem: The square of the hypotenuse is equal to the sum of the squares of the other two sides.
# The following code evaluates the length of the hypotenuse (i.e., the longest side of a right-angled triangle, the one opposite of the right angle) using the Pythagorean theorem:

a = 3.0
b = 4.0
c = (a ** 2 + b ** 2) ** 0.5
print("c =", c)